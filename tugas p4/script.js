function sumDigits(numStr) {
    return numStr.split('').reduce((acc, d) => acc + parseInt(d), 0);
}

function hitungGarisHidup() {
    const tanggal = parseInt(document.getElementById('tanggal').value);
    const bulan   = parseInt(document.getElementById('bulan').value);
    const tahun   = parseInt(document.getElementById('tahun').value);

    const allDigits = String(tanggal) + String(bulan) + String(tahun);
    const hasil1    = sumDigits(allDigits);

    let hasil2     = '';
    let hasilAkhir = hasil1;

    if (hasil1 > 9) {
        hasil2     = sumDigits(String(hasil1));
        hasilAkhir = hasil2;
        while (hasilAkhir > 9) {
            hasilAkhir = sumDigits(String(hasilAkhir));
        }
    }

    document.getElementById('outTanggal').value    = tanggal;
    document.getElementById('outBulan').value      = bulan;
    document.getElementById('outTahun').value      = tahun;
    document.getElementById('outHasil1').value     = hasil1;
    document.getElementById('outHasil2').value     = hasil2;
    document.getElementById('outHasilAkhir').value = hasilAkhir;

    const data = garisHidupData[hasilAkhir];
    if (data) {
        document.getElementById('resultTitle').textContent = data.title;
        const descDiv = document.getElementById('resultDesc');
        descDiv.innerHTML = '';
        data.desc.forEach(paragraph => {
            const p = document.createElement('p');
            p.textContent = paragraph;
            descDiv.appendChild(p);
        });
        document.getElementById('resultSection').style.display = 'block';
    }
}

document.addEventListener('DOMContentLoaded', () => {

    const tanggalSelect = document.getElementById('tanggal');
    for (let i = 1; i <= 31; i++) {
        const opt = document.createElement('option');
        opt.value = i;
        opt.textContent = i;
        tanggalSelect.appendChild(opt);
    }
    tanggalSelect.value = 1;

    const tahunSelect = document.getElementById('tahun');
    const currentYear = new Date().getFullYear();
    for (let y = currentYear; y >= 2000; y--) {
        const opt = document.createElement('option');
        opt.value = y;
        opt.textContent = y;
        if (y === 2008) opt.selected = true;
        tahunSelect.appendChild(opt);
    }

    hitungGarisHidup();

    document.getElementById('tanggal').addEventListener('change', hitungGarisHidup);
    document.getElementById('bulan').addEventListener('change', hitungGarisHidup);
    document.getElementById('tahun').addEventListener('change', hitungGarisHidup);
});